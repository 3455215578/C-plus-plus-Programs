#include <iostream>

int main(void)
{
	// char、short、int、long、long long
	// float double

	// ASCII
	char a = 'A';
	char b = 65;
	short c = 'A';

	std::cout << a << std::endl;
	std::cout << b << std::endl;
	std::cout << c << std::endl;

	// 存储小数
	float d = 5.5f; // 实际上是double类型, 要在数据后面加f

	// bool
	bool variable = 233;
	std::cout << variable << std::endl;

	return 0;
}